package preclass_materials_multidimensional_arrays_soru;

public class SunuSoruCozumleriSoru1 {
    public static void main(String[] args) {

        int arr[][] = {{1,2,3}, {4,5,6}};

        int sum = 1;

        for (int[] w: arr){


            for(int k : w){

                sum = sum*k;

            }

        }
        System.out.println(sum);










































































    }
}
